# Operators are used to perform operations on variables and values
# In the example below, we use the + operator to add together two values

# Arithmetic operators
x = 20
y = 6 
# Addition
print(x + y)

# Subtraction 
print(x - y)

# Multipication
print(x * y)

# Division
print(x/y)
# above gave float number

# Floor division [new]
print(x//y)

# Modulus
print(x%y)

# Exponentiation
a = 2
b = 4
print(a**b)

# Python Comparison Operators
m = 6
n = 4

# Equal to
print(m==n)

# Not equal
print(m!=n)

# Greater than
print(m>n)

# Less than
print(m<n)

# Greater than or equal to
print(m>=n)

# Less than or equal to
print(m<=n)